public class Op7_compoundAssigmentOperators {
    public static void main(String[] args) {

        int a = 5;
        a += 10;
        System.out.println(a);

        int b = 2;
        b *= 3;
        System.out.println(b);

        int c = 20;
        c %= 3;
        System.out.println(c);
    }
}
